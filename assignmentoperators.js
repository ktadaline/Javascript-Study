//assignment operators assign values to JavaScript variables

/*
Operator    Example   Is equivalent to

=           x = y     x = y
+=          x += y    x = x + y
-=          x -= y    x = x - y
*=          x *= y    x = x * y
/=          x /= y    x = x / y
%=          x %= y    x = x  % y
 */

